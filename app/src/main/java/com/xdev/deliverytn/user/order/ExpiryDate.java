package com.xdev.deliverytn.user.order;

public class ExpiryDate {

    public int year;
    public int month;
    public int day;

    public ExpiryDate() {

    }

    public ExpiryDate(int year, int month, int day) {

        this.year = year;
        this.month = month;
        this.day = day;
    }
}
